    public void tampilData() throws SQLException{
        try {
        String kolom[] = {"id_candidat","candidate_name","suara"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String query_tampil = "select * from voted";
        Statement st = koneksi.createStatement();
        ResultSet rs = st.executeQuery(query_tampil);

        while (rs.next()) {
            int id_candidat = rs.getInt("id_candidat");
            String candidate_name = rs.getString("candidate_name");
            int suara = rs.getInt("suara");
            Object data[] = {id_candidat, candidate_name, suara};
            dtm.addRow(data);
        }

        tabel.setModel(dtm);

        // Menutup ResultSet dan Statement setelah selesai digunakan
        rs.close();
        st.close();
    } catch (SQLException e) {
        e.printStackTrace(); // Menampilkan jejak eksepsi untuk membantu penanganan masalah
        // Atau Anda bisa menambahkan tindakan lain sesuai kebutuhan, seperti pesan kesalahan yang spesifik atau pemulihan.
    } catch (Exception e) {
        e.printStackTrace();
    }
        
    }
    
    
    
     
